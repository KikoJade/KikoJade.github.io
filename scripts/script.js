function darkMode() {
    document.getElementById('mainPage').style="background-color: #505050; color: #FEFEFE;";
    document.getElementById('title').style="background-color: #707070;";
}

function lightMode() {
    document.getElementById('mainPage').style="background-color: #FEFEFE; color: #101010;";
    document.getElementById('title').style="background-color: #DDDDDD;";
}

function defaultMode() {
    document.getElementById('mainPage').style="background-color: cadetblue; color: #000000;";
    document.getElementById('title').style="background-color: rgb(67, 90, 118);";
}